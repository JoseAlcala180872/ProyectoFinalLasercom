    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String nombreUsuario = txtNombreUsuario.getText().trim();
        String contrasena = new String(txtContrasena.getPassword());
        
        if (nombreUsuario.isEmpty() || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes llenar todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        UsuarioBO usuarioBO = new UsuarioBO();
        boolean acceso = usuarioBO.login(nombreUsuario, contrasena);
        
        if (acceso) {
            JOptionPane.showMessageDialog(this, "Bienvenido," + nombreUsuario + "!!!", "Exito", JOptionPane.INFORMATION_MESSAGE);
            // Aqui implementen el cambio de pantalla los del front.
        } else {
            JOptionPane.showMessageDialog(this, "Credenciales incorrectas", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

